void server_game_loop()
{
    game g = make_game("");
    send_board(g);

    message m;
    while (1) {
        do {
            m = receive_data();
        } while (m.type != M_UP
                && m.type != M_DOWN
                && m.type != M_RIGHT
                && m.type != M_LEFT);

        g.light_level += 1;
        if (g.light_level > 128)
            g.light_level = 128;

        send_board(g);
    }

}

void send_board(game g)
{
    int width = g.light_level * 2 + 1;
    char *buffer = malloc(width * width);

    int buffer_ptr = 0;
    for (int i = g.player_pos.x - g.light_level; i <= width; i ++)
        for (int j = g.player_pos.y - g.light_level; j <= width; j ++) {
            if (i > 0 && i < 64 && j > 0 && j < 64)
                buffer[buffer_ptr++] = g.board[i][j];
            else
                buffer[buffer_ptr++] = 'X';
        }

    message m = {width * width, M_VIS, buffer};
    send_data(m);
    delete_message(&m);
}

void client_game_loop()
{
    while (1) {
        message m;
        do {
            m = receive_data();
        } while (m.type != M_VIS);

        render_board(m.data, m.size);

        char move;
        scanf("%c", &move);

        switch(move) {
            case 'w':
                send_message((message){0, M_UP, NULL});
                break;
            
            case 's':
                send_message((message){0, M_DOWN, NULL});
                break;
        
            case 'a':
                send_message((message){0, M_LEFT, NULL});
                break;
    
            case 'd':
                send_message((message){0, M_RIGHT, NULL});
                break;
        }
    }
}

void render_board(char *board, size_t size)
{
    if (!board) {
        fprintf(stderr, "Tabuleiro inválido\n");
        return;
    }

    size_t width = sqrt(size);

    if (size % width != 0) {
        fprintf(stderr, "O tabuleiro não é quadrado\n");
        return;
    }

    for (int i = 0; i < size; i += width) {
        for (int j = 0; j < width; j ++)
            printf("%c", board[i + j]);
        printf("\n");
    }
}